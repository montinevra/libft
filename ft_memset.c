#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*dst;
	size_t	i;

	dst = b;
	i = 0;
	while (i < len)
	{
		dst[i] = c;
		i++;
	}
	return (b);
}
