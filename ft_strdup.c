#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*copy;

	len = ft_strlen(s1) + 1;
	if (!(copy = (char *)malloc(sizeof(char) * len)))
		return (NULL);
	ft_strcpy(copy, s1);
	return (copy);
}
